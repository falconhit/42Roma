int	ft_str_is_uppercase(char *str)
{
	unsigned int	i;

	i = 0;
	while (*str != '\0')
	{
		if (*str < 'A' || *str > 'Z')
		{
			i++;
		}
		str++;
	}
	if (i > 0)
	{
		return (0);
	}
	else
		return (1);
}
