char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	if (to_find == '\0')
		return (str);
	while (str[i] != '\0')
	{
		if (str[i] == to_find[y])
			i++;
		y++;
	}
	return (&str[i + y]);
}
